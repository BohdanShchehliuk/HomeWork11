package Task1;

public class Car {
    public int year;
    public String colour;

    public Car() {
        this.year = 1990;
        this.colour = "blu";
    }

    public Car(int year) {
        this.year = year;
        this.colour = "white";
    }

    public Car(int year, String colour) {
        this.year = year;
        this.colour = colour;
    }


}

